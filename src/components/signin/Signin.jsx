const useStyles = makeStyles(theme => ({
    '@global': {
        body: {
            backgroundColor: theme.palette.common.white,
        },
    },
    paper: {
        marginTop: theme.spacing(8),
        display: 'flex',
        flexDirection: 'column',
        alignItems: 'center',
    },
    avatar: {
        margin: theme.spacing(1),
        backgroundColor: theme.palette.secondary.main,
    },
    form: {
        width: '100%', // Fix IE 11 issue.
        marginTop: theme.spacing(1),
    },
    submit: {
        margin: theme.spacing(3, 0, 2),
    },
}));

export default function SignIn(props) {
    const classes = useStyles();
    const [user, setUser] = useState({});

    const onSumbit = event => {
        event.preventDefault();
        if (user.username && user.password) {
            console.log('This is what is being sent to ', user)
            // const decode = jwtDecode(localStorage.getItem('token'))
            axios.post(`http://localhost:5000/users/login`, user)
                .then(res => {
                    console.log('Logged in', res)
                    localStorage.setItem('token', res.data.token)
                    if (!props.location.state || props.location.state.prevLocation === '/') { //if state does not exist (hard refresh/first page user goes to) or as long as previous page wasn't root
                        props.history.push('/home')
                    } else props.history.goBack()
                })
                .catch(err => console.log('You failed to Login', err))
        } else {
            console.log('Validate')
        }
    }

    const handleChange = event => {
        const updatedUser = { ...user, [event.target.name]: event.target.value }
        setUser(updatedUser)
        console.log(updatedUser)
    }

    return (
        <Container component="main" maxWidth="xs">
            <CssBaseline />
            <div className={classes.paper}>
                <Avatar className={classes.avatar}>
                    <LockOutlinedIcon />
                </Avatar>
                <Typography component="h1" variant="h5">
                    Sign in
            </Typography>
                <form className={classes.form} Validate>
                    <TextField
                        variant="outlined"
                        margin="normal"
                        required
                        fullWidth
                        id="username"
                        label="Username"
                        name="username"
                        autoComplete="username"
                        autoFocus
                        onChange={handleChange}
                    />
                    <TextField
                        variant="outlined"
                        margin="normal"
                        required
                        fullWidth
                        name="password"
                        label="Password"
                        type="password"
                        id="password"
                        autoComplete="current-password"
                        onChange={handleChange}
                    />
                    <FormControlLabel
                        control={<Checkbox value="remember" color="primary" />}
                        label="Remember me"
                    />
                    <Button
                        type="submit"
                        fullWidth
                        variant="contained"
                        color="primary"
                        className={classes.submit}
                        onClick={onSumbit}
                    >
                        Sign In
            </Button>
                    <Grid container>

                        <Grid item>
                            <Link href="http://localhost:3000/" variant="body2">
                                {"Don't have an account? Sign Up"}
                            </Link>
                        </Grid>
                    </Grid>
                </form>
            </div>
            <Box mt={8}>
                <Copyright />
            </Box>
        </Container>

    );
}